def accum(s: str):
    # Write your code here.

    def get_apb(index, alphabet):
        return alphabet.upper() + (alphabet.lower() * index)

    len_s = len(s)
    result_before_merge = []

    for i in range(len_s):
        text = get_apb(i, s[i])
        result_before_merge.append(text)

    result = "-".join(result_before_merge)

    return result


